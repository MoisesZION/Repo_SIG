public class dosColumnas {

    public static void main(String[] args) {

        int a = 1;    
        int b = 100; 

        System.out.println("Columna 1    Columna 2");

        while (a <= 100) {
            System.out.println(a + "            " + b);
            a++;
            b--;
        }
    }
}
